/**
 * CONTEXTO DE TEMA (DARK MODE)
 * 
 * Este contexto maneja el estado global del tema de la aplicación
 * Permite cambiar entre modo claro y oscuro en toda la aplicación
 */

import { createContext, useContext, useState, useEffect, ReactNode } from 'react';

interface ThemeContextType {
  isDark: boolean;
  toggleTheme: () => void;
}

const ThemeContext = createContext<ThemeContextType | undefined>(undefined);

export function ThemeProvider({ children }: { children: ReactNode }) {
  const [isDark, setIsDark] = useState(false);

  // Cargar preferencia de tema del localStorage
  useEffect(() => {
    const savedTheme = localStorage.getItem('unibe-theme');
    if (savedTheme === 'dark') {
      setIsDark(true);
      document.documentElement.classList.add('dark');
    }
  }, []);

  const toggleTheme = () => {
    setIsDark(!isDark);
    if (!isDark) {
      document.documentElement.classList.add('dark');
      localStorage.setItem('unibe-theme', 'dark');
    } else {
      document.documentElement.classList.remove('dark');
      localStorage.setItem('unibe-theme', 'light');
    }
  };

  return (
    <ThemeContext.Provider value={{ isDark, toggleTheme }}>
      {children}
    </ThemeContext.Provider>
  );
}

export function useTheme() {
  const context = useContext(ThemeContext);
  if (context === undefined) {
    throw new Error('useTheme must be used within a ThemeProvider');
  }
  return context;
}
